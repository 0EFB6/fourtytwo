#include <unistd.h>

void ft_print_comb(void)
{
	int i;
	int j;
	int k;
    char c[4];

    for (i = 0; i < 10; i++)
    {
        for (j = i + 1; j < 10; j++)
        {
            for (k = j + 1; k < 10; k++)
            {
                c[0] = i + '0';
                c[1] = j + '0';
                c[2] = k + '0';
                c[3] = ',';
                write(1, &c, 4);
            }
        }
    }
}

int main()
{
	ft_print_comb();
}
