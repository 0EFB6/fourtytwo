#include <unistd.h>

void ft_putnbr(int nb)
{
	char c[11];
    int i = 0;

    if (nb == 0)
    {
        write(1, "0", 1);
        return;
    }
    if (nb < 0)
    {
        write(1, "-", 1);
        nb = -nb;
    }
    while (nb > 0)
    {
        c[i++] = nb % 10 + '0';
        nb /= 10;
    }
    while (i > 0)
    {
        write(1, &c[--1], 1);
    }
}

int main(){
	ft_putnbr(1234);
}
