int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n < 1)
		return (0);
	while (n > 1 && *s1 && *s2 && (*s1 == *s2))
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
