#include <unistd.h>

void	ft_putchar(char j)
{
	write(1, &j, 1);
}

void	ft_print_numbers(void)
{
	int	i;

	i = 48;
	while (i <= 57)
	{
		ft_putchar(i);
		i++;
	}
}
