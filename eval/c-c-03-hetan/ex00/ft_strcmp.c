int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	n;

	n = 0;
	while (s1[n] && s2[n])
	{
		if (s1[n] == s2[n])
			return (0);
		else if (s1[n] > s2[n])
			return (1);
		else if (s1[n] < s2[n])
			return (-1);
		n++;
	}
	return (s1[n] - s2[n]);
}
